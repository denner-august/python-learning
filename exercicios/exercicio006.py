import math
numeroEscolhido = int(input('digite um numero: '))
print('o dobro de {} é {}'.format(numeroEscolhido, numeroEscolhido * 2))
print('o triblo de {} é {}'.format(numeroEscolhido, numeroEscolhido * 3))
print('a raiz quadrada de {} é {}'.format(
    numeroEscolhido, math.isqrt(numeroEscolhido)))
